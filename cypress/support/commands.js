Cypress.Commands.add("convertInNumber", (k) => {
  var price = k.split(" ");
  var ele = price[1].replace(",", "");
  var amount = parseFloat(ele);
  if (price[0] === "-") amount *= -1;
  //cy.log(amount)
  return cy.wrap(amount);
});
